#include<stdio.h>

int main() {

    printf("sum of 2 & 3 : %d\n", 2 + 3);               // int + int = int = %d

    printf("sum of 2.0 & 3 : %f\n", 2.0 + 3);           // double + int = double = %f

    printf("sum of 2.0 & 3.0 : %.2f\n", 2.0 + 3.0);       // double + double = double = %f

    return 0;
}